function[bin] = de2bi(dec)

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
%   [bin] = de2bi(dec)
%
%       bin     : 2進数配列
%       dec     : 10進　整数値
%                                                       16.04.13 by.OKB
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

% %%
% clear all
% dec = 10;

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

bi = nan(1,16);
de = dec;
i = 0;
while de ~= 0
    i = i+1;
    
    if rem(de,2) == 1;
        bi(1,i) = 1;
    else
        bi(1,i) = 0;
    end
    
    de = floor(de/2);
end

bin = bi(1,i:-1:1);

end